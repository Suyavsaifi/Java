public class DataTypes {
    public static void main(String[] args){
        // this class define and print primitite data types
        boolean isJavaFun = true;
        byte myCredit = 3;
        char grade = 'A';
        short numCourses = 4;
        int age = 19;
        long tutionPaid = 2386L;
        float gpa = 7.6f;
        double incomeEarned = 8675.897d;

        System.out.println("Is Java fun to learn?: " + isJavaFun);
        System.out.println("Number of credits for the course: " + myCredit);
        System.out.println("The student's grade in the course: " + grade);
        System.out.println("The number of courses student has enrolled in: " + numCourses);
        System.out.println("The age of the student: " + age);
        System.out.println("Tution fee paid by student: " + tutionPaid);
        System.out.println("Total GPA student has scored in exam: " + gpa);
        System.out.println("The package student is expected to get: " + incomeEarned);
    }
}